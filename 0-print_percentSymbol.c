#include "main.h"

/**
 * printPercentSymbol - Prints the '%' character using _putchar.
 * 
 * This function utilizes the _putchar function to print the '%' character
 * to the standard output. The '%' character is represented by the ASCII code 37.
 * 
 * Return: Always returns 1.
 */
int printPercentSymbol(void)
{
	_putchar(37);    /* Call _putchar to print the '%' character to the standard output. */
	return (1);      /* Return 1 to indicate successful execution of the function. */
}

